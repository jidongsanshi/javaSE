package com.jidongsanshi.variable;

public class VariableDemo01 {
    public static void main(String[] args) {
        //数据类型 数据名称 = 初始值;
        double money = 6.0;
        System.out.println(money);
        money += 4.0;
        System.out.println(money);
        System.out.println("====================================");
        int age =21 ;
        System.out.println(age);
        age=25;
        System.out.println(age);
    }
}
